#!/usr/bin/env python3
"""
Module: 5-sum_list.py

This module contains a function that returns sum of list as
a float
"""


from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    this function takes a list input_list of floats as an
    argument and returns their sum as float
    """
    return sum(input_list)
